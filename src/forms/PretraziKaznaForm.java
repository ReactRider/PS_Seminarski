    private void prepareKategorija() {
        this.add(lblKategorija1);
        this.add(comboKategorija1);
        ucitajKategorije("Kategorija");
        
        comboKategorija1.addActionListener( e -> {
            if(comboKategorija1.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Izaberite kategoriju kazne!", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            this.add(jScrollPane1);
            tblKazne.setModel(new KaznaTableModel(new Kazna((KategorijaKazna)comboKategorija1.getSelectedItem())));
            
        });
        
    }
    
    private void prepareNaziv() {
        this.add(lblNaziv);
        this.add(txtNazivKazna);
        this.add(btnPretragaKazna);
        this.add(lblKategorija);
        this.add(comboKategorija);
        comboKategorija.setEnabled(false);
        this.add(lblIznos);
        this.add(txtIznos);
        txtIznos.setEnabled(false);
        
        
        btnPretragaKazna.addActionListener( e -> {
            if(!check_naziv()) {
                JOptionPane.showMessageDialog(this, "Neispravan unos naziva kazne", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Kazna k = Controller.getInstance().pretraziKazna(new Kazna(txtNazivKazna.getText()));
            if(k != null) {
                ucitajKategorije("Naziv");
                comboKategorija.setSelectedItem(k.getKategorija());
                comboKategorija.setEnabled(false);
                txtIznos.setText(k.getIznos()+"");
                txtIznos.setEnabled(false);
            } else {
                comboKategorija.setSelectedItem(null);
                txtIznos.setText("");
                JOptionPane.showMessageDialog(this, "Nema rezultata pretrage!", "Poruka", JOptionPane.INFORMATION_MESSAGE);
            }
        });
    }
    
