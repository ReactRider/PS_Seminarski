    private void ucitajKategorije(String s) {
        switch(s) {
            case "Naziv":
                for(KategorijaKazna elem : KategorijaKazna.values())
                comboKategorija.addItem(elem);
                comboKategorija.setSelectedItem(null);
            break;

            case "Kategorija":
            for(KategorijaKazna elem : KategorijaKazna.values())
                comboKategorija1.addItem(elem);

            comboKategorija1.setSelectedItem(null);
            break;
        }
        
    }
    
    private boolean check_naziv() {
        String naziv_kazne = txtNazivKazna.getText();
        if(naziv_kazne.equals(""))
            return false;
        
        return true;
    }

