    private void prepareGeneral() {
        this.remove(lblGrad);
        this.remove(comboGrad);
        this.remove(jScrollPane1);
        this.remove(lblRaskrsnica);
        this.remove(comboRaskrsnica);
        this.remove(jScrollPane2);
    }
    
    public PretraziPUKritForm(java.awt.Frame parent, boolean modal, String kriterijum) {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(null);
        setTitle("Pretrazi Policijske Uprave");
        
        prepareGeneral();
        
        if(kriterijum.equals("Lokacija")) 
            prepareLokacija();
        else if(kriterijum.equals("Raskrsnica")) 
            prepareRaskrsnica();
    }
        
    private void prepareLokacija() {
        this.add(lblGrad);
        this.add(comboGrad);
        ucitajGradove();
        
        comboGrad.addActionListener( e -> {
            if(comboGrad.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Izaberite grad pu", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            this.add(jScrollPane1);
            tblUprave.setModel(new PUTableModel(new PolicijskaUprava((String)comboGrad.getSelectedItem(), 0)));
        });
    }
    
    private void prepareRaskrsnica() {
        this.add(lblRaskrsnica);
        this.add(comboRaskrsnica);
        ucitajRaskrsnice();
        
        comboRaskrsnica.addActionListener( e -> {
            if(comboRaskrsnica.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Izaberite raskrsnicu", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            this.add(jScrollPane2);
            tblUprave1.setModel(new PUTableModel(new Raskrsnica((String) comboRaskrsnica.getSelectedItem(), "") ));
        });
    }
    
    
    private void ucitajRaskrsnice() {
        ArrayList<String> raskrsnice = Controller.getInstance().ucitajRaskrsnice();
        
        for(String r : raskrsnice)
            comboRaskrsnica.addItem(r);
        comboRaskrsnica.setSelectedItem(null);
    }
    
    
    private void ucitajGradove() {
        ArrayList<String> gradovi = Controller.getInstance().ucitajGradove();
        
        for(String grad : gradovi)
            comboGrad.addItem(grad);
        comboGrad.setSelectedItem(null);
    }
    
